class Additionpolymorphism{
	public int additionInteger(int num1, int num2){
		return num1+num2;
	}
	public int additionInteger(int num1, int num2, int num3){
	return num1+num2+ num3;
	}
	
	public int additionInteger(int num1, int num2, int num3, int num4){
		return num1+num2+num3+num4;
	}
	
	public double additionInteger(double num1, double num2){
		return num1+num2;
	}
	}