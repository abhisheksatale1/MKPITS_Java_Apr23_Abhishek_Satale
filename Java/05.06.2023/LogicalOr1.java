class LogicalOr1{
	// logical operator using two parameter
	
	public boolean LogicalOr1Operator(boolean var1, boolean var2){
		return var1 || var2;
	}
	
	public boolean LogicalOr1Operator(boolean var1, boolean var2, boolean var3){
		return var1 || var2 || var3;
	}
	
	public boolean LogicalOr1Operator(boolean var1, boolean var2, boolean var3, boolean var4){
		return var1 || var2 || var3 || var4;
	}
	
	public boolean LogicalOr1Operator(boolean var1, boolean var2, boolean var3, boolean var4, boolean var5){
		return var1 || var2 || var3 || var4 || var5;
	}
}