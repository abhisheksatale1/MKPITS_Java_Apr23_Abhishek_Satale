package org.example;

public class Cube {
    public void squarecube(int number1, int number2){

        System.out.println(number1*number1*number1);
        System.out.println(number2*number2*number2);

    }

    public void squarecube(Long number1,Long number2){
        System.out.println(number2*number2*number2);
        System.out.println(number1*number1*number1);


    }
}
